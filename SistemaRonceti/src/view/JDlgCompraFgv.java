    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        CompraFgv comprafgv = viewBean();
        CompraFgv_DAO comprafgv_DAO = new CompraFgv_DAO();
        if (incluindo == true) {
            comprafgv_DAO.insert(comprafgv);
        } else {
            comprafgv_DAO.update(comprafgv);
        }

        Util.habilitar(true, jTxtCodigo, jTxtNome, jTxtCodigo, jTxtNome, jTxtValor,jTxtQuantidade,jTxtStatus,
         jBtnCancelar, jBtnConfirmar);

        Util.habilitar(false, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);

        Util.limparCampos(jTxtCodigo, jTxtNome, jTxtValor,jTxtQuantidade,jTxtStatus);

    }//GEN-LAST:event_jBtnConfirmarActionPerformed

